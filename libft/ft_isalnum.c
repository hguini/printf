#include "libft.h"

int	ft_isalnum(int sym)
{
	if ((sym >= 'A' && sym <= 'Z') || (sym >= 'a' && sym <= 'z'))
		return (1);
	if (sym >= '0' && sym <= '9')
		return (1);
	else
		return (0);
}
